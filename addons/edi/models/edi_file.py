# -*- encoding: utf-8 -*-

from odoo import fields, models


class EdiFile(models.Model):
    """
    Represents an edi file, such as xml's, pdf's, etc.
    """
    _name = 'edi.file'
    _description = 'Files linked to an edi flow'

    edi_flow_id = fields.Many2one(
        comodel_name='edi.flow',
        ondelete='cascade',
    )
    attachment_id = fields.Many2one(
        comodel_name='ir.attachment',
        help='The attachment generated by the EDI for this particular document.',
        required=True,
        ondelete='cascade',
    )
    code = fields.Char(
        help='A unique code used to identify this document in a flow.',
        required=True,
    )
