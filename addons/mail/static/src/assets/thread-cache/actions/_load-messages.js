/** @odoo-module **/

import { Define } from '@mail/define';

export default Define`
    {Record/insert}
        [Record/models]
            Action
        [Action/name]
            ThreadCache/_loadMessages
        [Action/params]
            threadCache
                [type]
                    ThreadCache
            limit
                [type]
                    Integer
                [default]
                    30
            maxId
                [type]
                    Integer
            minId
                [type]
                    Integer
        [Action/returns]
            Collection<Message>
        [Action/description]
            @throws {Error} when failed to load messages
        [Action/behavior]
            {Record/update}
                [0]
                    @threadCache
                [1]
                    [ThreadCache/isLoading]
                        true
            {try}
                :messages
                    {Message/performRpcMessageFetch}
                        [0]
                            @threadCache
                            .{ThreadCache/thread}
                            .{Thread/fetchMessagesUrl}
                        [1]
                            [limit]
                                @limit
                            [max_id]
                                @maxId
                            [min_id]
                                @minId
            .{catch}
                {Record/insert}
                    [Record/models]
                        Function
                    [Function/in]
                        error
                    [Function/out]
                        {if}
                            {Record/exists}
                                @threadCache
                        .{then}
                            {Record/update}
                                [0]
                                    @threadCache
                                [1]
                                    [ThreadCache/hasLoadingFailed]
                                        true
                                    [ThreadCache/isLoading]
                                        false
                        {Error/raise}
                            @error
            {if}
                {Record/exists}
                    @threadCache
                .{isFalsy}
            .{then}
                {break}
            {Record/update}
                [0]
                    @threadCache
                [1]
                    [ThreadCache/fetchedMessages]
                        {Field/add}
                            @messages
                    [ThreadCache/hasLoadingFailed]
                        false
                    [ThreadCache/isLoaded]
                        true
                    [ThreadCache/isLoading]
                        false
            {if}
                @minId
                .{isFalsy}
                .{&}
                    @messages
                    .{Collection/length}
                    .{<}
                        @limit
            .{then}
                {Record/update}
                    [0]
                        @threadCache
                    [1]
                        [ThreadCache/isAllHistoryLoaded]
                            true
            {Env/messagingBus}
            .{Bus/trigger}
                [0]
                    o-thread-cache-loaded-messages
                [1]
                    [fetchedMessages]
                        @messages
                    [threadCache]
                        @threadCache
            @messages
`;
