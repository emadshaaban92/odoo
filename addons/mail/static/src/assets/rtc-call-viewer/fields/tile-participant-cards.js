/** @odoo-module **/

import { Define } from '@mail/define';

export default Define`
    {Dev/comment}
        List of all participant cards (can either be invitations or rtcSessions).
    {Record/insert}
        [Record/models]
            Field
        [Field/name]
            tileParticipantCards
        [Field/model]
            RtcCallViewer
        [Field/type]
            many
        [Field/target]
            RtcCallParticipantCard
        [Field/inverse]
            RtcCallParticipantCard/rtcCallViewerOfTitle
        [Field/compute]
            {if}
                @record
                .{RtcCallViewer/threadView}
                .{isFalsy}
            .{then}
                {Record/empty}
                {break}
            :tileCards
                {Record/insert}
                    [Record/models]
                        Collection
            :sessionPartners
                {Record/insert}
                    [Record/models]
                        Set
            :sessionGuests
                {Record/insert}
                    [Record/models]
                        Set
            {foreach}
                @record
                .{RtcCallViewer/threadView}
                .{ThreadView/thread}
                .{Thread/rtcSessions}
            .{as}
                rtcSession
            .{do}
                {if}
                    @rtcSession
                    .{RtcSession/partner}
                .{then}
                    {Set/add}
                        [0]
                            @sessionPartners
                        [1]
                            @rtcSession
                            .{RtcSession/partner}
                            .{Partner/id}
                {if}
                    @rtcSession
                    .{RtcSession/guest}
                .{then}
                    {Set/add}
                        [0]
                            @sessionPartners
                        [1]
                            @rtcSession
                            .{RtcSession/guest}
                            .{Guest/id}
                {Collection/push}
                    [0]
                        @tileCards
                    [1]
                        [Record/models]
                            RtcCallParticipantCard
                        [RtcCallParticipantCard/relationalId]
                            rtc_session_
                            .{+}
                                @rtcSession
                                .{Record/id}
                            .{+}
                                _
                            .{+}
                                @record
                                .{RtcCallViewer/threadView}
                                .{ThreadView/thread}
                                .{Record/id}
                        [RtcCallParticipantCard/rtcSession]
                            @rtcSession
                        [RtcCallParticipantCard/channel]
                            @record
                            .{RtcCallViewer/threadView}
                            .{ThreadViewer/thread}
            {foreach}
                @record
                .{RtcCallViewer/threadView}
                .{ThreadView/thread}
                .{Thread/invitedPartners}
            .{as}
                partner
            .{do}
                {if}
                    {Set/has}
                        [0]
                            @sessionPartners
                        [1]
                            @partner
                            .{Partner/id}
                .{then}
                    {continue}
                {Collection/push}
                    [0]
                        @tileCards
                    [1]
                        [Record/models]
                            RtcCallParticipantCard
                        [RtcCallParticipantCard/relationalId]
                            invited_partner_
                            .{+}
                                @partner
                                .{Record/id}
                            .{+}
                                _
                            .{+}
                                @record
                                .{RtcCallViewer/threadView}
                                .{ThreadView/thread}
                                .{Record/id}
                        [RtcCallParticipantCard/invitedPartner]
                            @partner
                        [RtcCallParticipantCard/channel]
                            @record
                            .{RtcCallViewer/threadView}
                            .{ThreadView/thread}
            {foreach}
                @record
                .{RtcCallViewer/threadView}
                .{ThreadView/thread}
                .{Thread/invitedGuests}
            .{as}
                guest
            .{do}
                {if}
                    {Set/has}
                        [0]
                            @sessionGuests
                        [1]
                            @guest
                            .{Guest/id}
                .{then}
                    {continue}
                {Collection/push}
                    [0]
                        @tileCards
                    [1]
                        [Record/models]
                            RtcCallParticipantCard
                        [RtcCallParticipantCard/relationalId]
                            invited_guest_
                            .{+}
                                @guest
                                .{Record/id}
                            .{+}
                                _
                            .{+}
                                @record
                                .{RtcCallViewer/threadView}
                                .{ThreadView/thread}
                                .{Record/id}
                        [RtcCallParticipantCard/invitedGuest]
                            @guest
                        [RtcCallParticipantCard/channel]
                            @record
                            .{RtcCallViewer/threadView}
                            .{ThreadView/thread}
            {Record/insert}
                @tileCards
`;
