/** @odoo-module **/

import { Define } from '@mail/define';

export default Define`
    {Dev/comment}
        Show (editable) list of subtypes of this follower.
    {Record/insert}
        [Record/models]
            Action
        [Action/name]
            Follower/showSubtypes
        [Action/params]
            follower
                [type]
                    Follower
        [Action/behavior]
            :subtypesData
                {Record/doAsync}
                    [0]
                        @follower
                    [1]
                        @env
                        .{Env/owlEnv}
                        .{Dict/get}
                            services
                        .{Dict/get}
                            rpc
                        .{Function/call}
                            [route]
                                /mail/read_subscription_data
                            [params]
                                [follower_id]
                                    @follower
                                    .{Follower/id}
            {Record/update}
                [0]
                    @follower
                [1]
                    [Follower/subtypes]
                        {Record/empty}
            {foreach}
                @subtypesData
            .{as}
                data
            .{do}
                :subtype
                    {Record/insert}
                        [Record/models]
                            FollowerSubtype
                        {FollowerSubtype/convertData}
                            @data
                {Record/update}
                    [0]
                        @follower
                    [1]
                        [Follower/subtypes]
                            {Field/add}
                                @subtype
                {if}
                    @data
                    .{Object/get}
                        followed
                .{then}
                    {Record/update}
                        [0]
                            @follower
                        [1]
                            [Follower/selectedSubtypes]
                                {Field/add}
                                    @subtype
                .{else}
                    {Record/update}
                        [0]
                            @follower
                        [1]
                            [Follower/selectedSubtypes]
                                {Field/remove}
                                    @subtype
            {Record/update}
                [0]
                    @record
                [1]
                    [Follower/followerSubtypeListDialog]
                        {Record/insert}
                            [Record/models]
                                Dialog
`;
