import { Define } from '@mail/define';

export default Define`
    {Dev/comment}
        Inserts text content in text input based on selection.
    {Record/insert}
        [Record/models]
            Action
        [Action/name]
            ComposerView/insertIntoTextInput
        [Action/params]
            record
                [type]
                    ComposerView
            content
                [type]
                    String
        [Action/behavior]
            :partA
                @record
                .{ComposerView/composer}
                .{ComposerView/textInputContent}
                .{String/slice}
                    [0]
                        0
                    [1]
                        @record
                        .{ComposerView/composer}
                        .{Composer/textInputCursorStart}
            :partB
                @record
                .{ComposerView/composer}
                .{Composer/textInputContent}
                .{String/slice}
                    [0]
                        @record
                        .{ComposerView/composer}
                        .{Composer/textInputCursorEnd}
                    [1]
                        @record
                        .{ComposerView/composer}
                        .{Composer/textInputContent}
                        .{String/length}
            :suggestionDelimiterPosition
                @record
                .{ComposerView/suggestionDelimiterPosition}
            {if}
                @suggestionDelimiterPosition
                .{!=}
                    undefined
                .{&}
                    @suggestionDelimiterPosition
                    .{>=}
                        @record
                        .{ComposerView/composer}
                        .{Composer/textInputCursorStart}
            .{then}
                :suggestionDelimiterPosition
                    @suggestionDelimiterPosition
                    .{+}
                        @content
                        .{String/length}
            {Record/update}
                [0]
                    @record
                    .{ComposerView/composer}
                [1]
                    [Composer/isLastStateChangeProgrammatic]
                        true
                    [Composer/textInputContent]
                        @partA
                        .{+}
                            @content
                        .{+}
                            @partB
                    [Composer/textInputCursorEnd]
                        @record
                        .{ComposerView/composer}
                        .{Composer/textInputCursorStart}
                        .{+}
                            @content
                            .{String/length}
                    [Composer/textInputCursorStart]
                        @record
                        .{ComposerView/composer}
                        .{Composer/textInputCursorStart}
                        .{+}
                            @content
                            .{String/length}
            {Record/update}
                [0]
                    @record
                [1]
                    [ComposerView/suggestionDelimiterPosition]
                        @suggestionDelimitedPosition
`;
