import { Define } from '@mail/define';

export default Define`
    {Record/insert}
        [Record/models]
            Field
        [Field/name]
            displayName
        [Field/model]
            Thread
        [Field/type]
            attr
        [Field/target]
            String
        [Field/compute]
            {if}
                @record
                .{Thread/channelType}
                .{=}
                    chat
                .{&}
                    @record
                    .{Thread/correspondent}
            .{then}
                @record
                .{Thread/customChannelName}
                .{|}
                    @record
                    .{Thread/correspondent}
                    .{Partner/nameOrDisplayName}
            .{elif}
                @record
                .{Thread/channelType}
                .{=}
                    group
                .{&}
                    @record
                    .{Thread/name}
                    .{isFalsy}
            .{then}
                :partnerNames
                    @record
                    .{Thread/members}
                    .{Collection/map}
                        {Record/insert}
                            [Record/models]
                                Function
                            [Function/in]
                                partner
                            [Function/out]
                                @partner
                                .{Partner/nameOrDisplayName}
                :guestNames
                    @record
                    .{Thread/guestMembers}
                    .{Collection/map}
                        {Record/insert}
                            [Record/models]
                                Function
                            [Function/in]
                                guest
                            [Function/out]
                                @guest
                                .{Guest/name}
                {Record/insert}
                    [Record/models]
                        Collection
                    []
                        @partnerNames
                    []
                        @guestNames
                    .{Collection/join}
                        {Locale/text}
                            , 
            .{else}
                @record
                .{Thread/name}
`;
