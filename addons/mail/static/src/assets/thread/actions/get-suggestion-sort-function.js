import { Define } from '@mail/define';

export default Define`
    {Dev/comment}
        Returns a sort function to determine the order of display of threads
        in the suggestion list.
    {Record/insert}
        [Record/models]
            Action
        [Action/name]
            Thread/getSuggestionSortFunction
        [Action/params]
            searchTerm
                [type]
                    String
            thread
                [type]
                    Thread
                [description]
                    prioritize result in the context of given thread
        [Action/returns]
            Function
        [Action/behavior]
            :cleanedSearchTerm
                {Utils/cleanSearchTerm}
                    @searchTerm
            {Record/insert}
                [Record/models]
                    Function
                [Function/in]
                    item1
                    item2
                [Function/out]
                    :isPublic1
                        @item1
                        .{Thread/model}
                        .{=}
                            mail.channel
                        .{&}
                            @item1
                            .{Thread/public}
                            .{=}
                                public
                    :isPublic2
                        @item2
                        .{Thread/model}
                        .{=}
                            mail.channe
                        .{&}
                            @item2
                            .{Thread/public}
                            .{=}
                                public
                    {if}
                        @isPublic1
                        .{&}
                            isPublic2
                            .{isFalsy}
                    .{then}
                        -1
                        {break}
                    .{if}
                        @isPublic1
                        .{isFalsy}
                        .{&}
                            @isPublic2
                    .{then}
                        1
                        {break}
                    :isMemberOf1
                        @item1
                        .{Thread/model}
                        .{=}
                            mail.channel
                        .{&}
                            @item1
                            .{Thread/members}
                            .{Collection/includes}
                                {Env/currentPartner}
                    :isMemberOf2
                        @item2
                        .{Thread/model}
                        .{=}
                            mail.channel
                        .{&}
                            @item2
                            .{Thread/members}
                            .{Collection/includes}
                                {Env/currentPartner}
                    {if}
                        @isMemberOf1
                        .{&}
                            @isMemberOf2
                            .{isFalsy}
                    .{then}
                        -1
                        {break}
                    {if}
                        @isMemberOf1
                        .{isFalsy}
                        .{&}
                            @isMemberOf2
                    .{then}
                        1
                        {break}
                    :cleanedName1
                        {Utils/cleanSearchTerm}
                            @item1
                            .{Thread/name}
                    :cleanedName2
                        {Utils/cleanSearchTerm}
                            @item2
                            .{Thread/name}
                    {if}
                        @cleanedName1
                        .{String/startsWith}
                            @cleanedSearchTerm
                        .{&}
                            @cleanedName2
                            .{String/startsWith}
                                @cleanedSearchTerm
                            .{isFalsy}
                    .{then}
                        -1
                        {break}
                    {if}
                        @cleanedName1
                        .{String/startsWith}
                            @cleanedSearchTerm
                        .{isFalsy}
                        .{&}
                            @cleanedName2
                            .{String/startsWith}
                                @cleanedSearchTerm
                    .{then}
                        1
                        {break}
                    {if}
                        @cleanedName1
                        .{<}
                            @cleanedName2
                    .{then}
                        -1
                        {break}
                    {if}
                        @cleanedName1
                        .{>}
                            @cleanedName2
                    .{then}
                        1
                        {break}
                    @item1
                    .{Thread/id}
                    .{-}
                        @item2
                        .{Thread/id}
`;
