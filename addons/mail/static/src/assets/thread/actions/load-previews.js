/** @odoo-module **/

import { Define } from '@mail/define';

export default Define`
    {Dev/comment}
        Load the previews of the specified threads. Basically, it fetches the
        last messages, since they are used to display inline content of them.
    {Record/insert}
        [Record/models]
            Action
        [Action/name]
            Thread/loadPreviews
        [Action/params]
            threads
                [type]
                    Collection<Thread>
        [Action/behavior]
            :channelIds
                @threads
                .{Collection/reduce}
                    [0]
                        {Record/insert}
                            [Record/models]
                                Function
                            [Function/in]
                                acc
                                item
                            [Function/out]
                                {if}
                                    @item
                                    .{Thread/model}
                                    .{=}
                                        mail.channel
                                .{then}
                                    @acc
                                    .{Collection/concat}
                                        @thread
                                        .{Thread/id}
                                @acc
                    [1]
                        {Record/insert}
                            [Record/models]
                                Collection
            {if}
                @channelIds
                .{Collection/length}
                .{=}
                    0
            .{then}
                {break}
            :channelPreviews
                @env
                .{Env/owlEnv}
                .{Dict/get}
                    services
                .{Dict/get}
                    rpc
                .{Function/call}
                    [0]
                        [model]
                            mail.channel
                        [method]
                            channel_fetch_preview
                        [args]
                            {Record/insert}
                                [Record/models]
                                    Collection
                                @channelIds
                    [1]
                        [shadow]
                            true
            {Record/insert}
                [Record/models]
                    Message
                @channelPreviews
                .{Collection/filter}
                    {Record/insert}
                        [Record/models]
                            Function
                        [Function/in]
                            item
                        [Function/out]
                            @item
                            .{Dict/get}
                                last_message
                .{Collection/map}
                    {Record/insert}
                        [Record/models]
                            Function
                        [Function/in]
                            item
                        [Function/out]
                            {Message/convertData}
                                @item
                                .{Dict/get}
                                    last_message
`;
